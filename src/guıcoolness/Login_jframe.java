    private void onLoginClick()
    {
        String uname,pwd;
        boolean status=false;
        NewJFrame frame=new NewJFrame();
        
        uname=txUname.getText();
        pwd=String.valueOf(txPass.getPassword());
        
        Login_Model loginModel= new Login_Model();
        loginModel.setUname(uname);
        loginModel.setPass(pwd);

        
      Login_Controller model= new Login_Controller(conn);
         status= model.login(loginModel);
         
         if(status==true){
             JOptionPane.showMessageDialog(rootPane, "Succesfully", "Info", JOptionPane.INFORMATION_MESSAGE);
             frame.setVisible(true);
             this.dispose();
             
         }
         else if(txUname.getText().isEmpty() && String.valueOf(txPass.getPassword()).isEmpty()){
                               JOptionPane.showMessageDialog(rootPane, "Username and Password still empty", "Info", JOptionPane.INFORMATION_MESSAGE);

             
         }
         else if(txUname.getText().equals(""))
         {
                  JOptionPane.showMessageDialog(rootPane, "Username still empty", "Info", JOptionPane.INFORMATION_MESSAGE);
         }else if(String.valueOf(txPass.getPassword()).equals(""))
         {
                  JOptionPane.showMessageDialog(rootPane, "Password still empty", "Info", JOptionPane.INFORMATION_MESSAGE);
                  
         }
         else if (!txUname.getText().isEmpty()){
             isUsernameExist();
         }
         else if(!String.valueOf(txPass.getPassword()).isEmpty())
                 {
                     isPasswordExist(); 
                 }
         else{
               JOptionPane.showMessageDialog(rootPane, "Invalid Username and Password", "Info", JOptionPane.INFORMATION_MESSAGE);
             
         }
    
    
    }
    private boolean isUsernameExist() 
    {
                boolean status=false;

    try {
        String sql="SELECT username FROM tbLogin";
        pst=ConnectDB.getConnection().prepareStatement(sql);
        pst.setString(1, txUname.getText());
        rst=pst.executeQuery();
        
        if(rst.next())
        {
            status=true;
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Invalid Username");
        }
        rst.close();
    } catch (SQLException ex) {
        Logger.getLogger(Login_jframe.class.getName()).log(Level.SEVERE, null, ex);
    }
    return status;
    }
    private boolean isPasswordExist()
    {
        boolean status=false;
         try {
        String sql="SELECT password FROM tbLogin";
        pst=ConnectDB.getConnection().prepareStatement(sql);
        
        pst.setString(1, txPass.getPassword().toString());
        rst=pst.executeQuery();
        
        if(rst.next())
        {
            status=true;
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Invalid Password");
        }
        rst.close();
    } catch (SQLException ex) {
        Logger.getLogger(Login_jframe.class.getName()).log(Level.SEVERE, null, ex);
    }
        return status;
    }
    
